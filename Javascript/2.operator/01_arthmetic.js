/**
 * 사칙연산 : +, -, *, /, %, **
 */
console.log(3 + 5); // 8
console.log(3 - 5); // -2
console.log(3 * 5); // 15
console.log(3 / 5); // 0.6
console.log(3 % 5); // 3
console.log(3 ** 5); // 243

// 짝수, 홀수, 선택 : %
let a = 101; // 홀수
a = 100; //짝수
console.log((a % 2) ? "홀수" : "짝수");

//접합 연산자 : +
console.log(10 + 20); // 30
console.log('10' + 20); // 1020
console.log('합계 = ' + a);
console.log(`합계 = ${a}`);
